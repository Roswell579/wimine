using System;
using System.Threading.Tasks;
using GMap.NET;
using wmine.Models;
using wmine.Services;

namespace wmine.Tests
{
    /// <summary>
    /// Test simple et rapide du service de routing
    /// </summary>
    public static class QuickRouteTest
    {
        public static async Task Run()
        {
            Console.WriteLine("?????????????????????????????????????????????");
            Console.WriteLine("?   TEST RAPIDE - SERVICE DE ROUTING        ?");
            Console.WriteLine("?????????????????????????????????????????????");
            Console.WriteLine();

            var service = new RouteService();
            
            // Test simple : Toulon ? Nice
            var toulon = new PointLatLng(43.1242, 5.9280);
            var nice = new PointLatLng(43.7102, 7.2620);

            Console.WriteLine("?? Calcul d'itinéraire : Toulon ? Nice");
            Console.WriteLine("?????????????????????????????????????????");

            try
            {
                Console.Write("? Appel API OSRM... ");
                var route = await service.CalculateRouteAsync(toulon, nice, TransportType.Car);
                Console.WriteLine("OK ?");
                Console.WriteLine();

                if (route != null)
                {
                    Console.WriteLine($"? Distance: {route.FormattedDistance}");
                    Console.WriteLine($"? Durée  : {route.FormattedDuration}");
                    Console.WriteLine($"? Points : {route.Points.Count} coordonnées");
                    Console.WriteLine($"? Instructions: {route.Instructions.Count}");
                    Console.WriteLine();

                    // Afficher les 3 premiéres instructions
                    if (route.Instructions.Count > 0)
                    {
                        Console.WriteLine("?? Premiéres instructions:");
                        for (int i = 0; i < Math.Min(3, route.Instructions.Count); i++)
                        {
                            Console.WriteLine($"   {i + 1}. {route.Instructions[i]}");
                        }
                        Console.WriteLine();
                    }

                    Console.WriteLine("?????????????????????????????????????????????");
                    Console.WriteLine("?          ? TEST RéUSSI !                 ?");
                    Console.WriteLine("?????????????????????????????????????????????");
                }
                else
                {
                    Console.WriteLine("? Aucun itinéraire trouvé");
                }
            }
            catch (Exception ex)
            {
                Console.WriteLine("ERREUR ?");
                Console.WriteLine();
                Console.WriteLine($"Message: {ex.Message}");
                Console.WriteLine();
                Console.WriteLine("?????????????????????????????????????????????");
                Console.WriteLine("?          ? TEST éCHOUé                   ?");
                Console.WriteLine("?????????????????????????????????????????????");
            }

            Console.WriteLine();
            Console.WriteLine("Appuyez sur Entrée pour continuer...");
            Console.ReadLine();
        }
    }
}
